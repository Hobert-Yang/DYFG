// 題目號69, x的平方根
public class Test3 {
    public static void main(String[] args) {
        double number = 25.0; // 要计算平方根的数字

        // 使用Math.sqrt方法计算平方根
        double squareRoot = Math.sqrt(number);

        int num = (int) squareRoot;

        // 输出结果
        System.out.println(num);
    }

}
